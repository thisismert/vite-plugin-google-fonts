import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const GOOGLE_FONTS_V2_URL =
    'https://raw.githubusercontent.com/fontsource/google-font-metadata/main/data/google-fonts-v2.json'
const VARIABLE_FONTS_URL =
    'https://raw.githubusercontent.com/fontsource/google-font-metadata/main/data/variable.json'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const outputPath = path.resolve(__dirname, '../src/generated/font-catalog.ts')

function compareFamilies(a, b) {
    return a.family.localeCompare(b.family)
}

function toKey(family) {
    return family.replace(/ /g, '_')
}

function toArrayLiteral(values) {
    return `[${values.map((value) => JSON.stringify(value)).join(', ')}]`
}

async function fetchJson(url) {
    const response = await fetch(url)
    if (!response.ok) {
        throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`)
    }

    return response.json()
}

async function main() {
    const [staticCatalog, variableCatalog] = await Promise.all([
        fetchJson(GOOGLE_FONTS_V2_URL),
        fetchJson(VARIABLE_FONTS_URL),
    ])

    const variableFamilies = new Set(
        Object.values(variableCatalog).map((entry) => entry.family),
    )

    const families = Object.values(staticCatalog)
        .map((entry) => ({
            family: entry.family,
            weights: [...new Set(entry.weights)].sort((a, b) => a - b),
            styles: [...new Set(entry.styles)],
            subsets: [...new Set(entry.subsets)],
            category: entry.category,
            variable: variableFamilies.has(entry.family),
        }))
        .sort(compareFamilies)

    const lines = [
        '/* eslint-disable */',
        '// This file is generated by scripts/generate-font-catalog.mjs.',
        '// Do not edit manually.',
        '',
        'export interface GoogleFontCatalogEntry {',
        '    family: string',
        '    weights: readonly number[]',
        '    styles: readonly string[]',
        '    subsets: readonly string[]',
        '    category: string',
        '    variable: boolean',
        '}',
        '',
        'export const googleFontCatalog = {',
        ...families.map(
            (entry) =>
                `    ${toKey(entry.family)}: { family: ${JSON.stringify(entry.family)}, weights: ${toArrayLiteral(entry.weights)}, styles: ${toArrayLiteral(entry.styles)}, subsets: ${toArrayLiteral(entry.subsets)}, category: ${JSON.stringify(entry.category)}, variable: ${String(entry.variable)} },`,
        ),
        '} as const satisfies Record<string, GoogleFontCatalogEntry>',
        '',
    ]

    await mkdir(path.dirname(outputPath), { recursive: true })
    await writeFile(outputPath, lines.join('\n'))
    console.log(`Wrote ${families.length} families to ${outputPath}`)
}

main().catch((error) => {
    console.error(error)
    process.exitCode = 1
})
